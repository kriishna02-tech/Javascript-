//Print the length of a string.

let name = prompt("enter string: ");
console.log(name.length); // it will also count space ,special character

//Convert a string to uppercase.
console.log(name.toUpperCase());

//Convert a string to lowercase.
console.log(name.toLowerCase());

// Extract the first 5 characters from a string.
console.log(name.substring(0,5));

//Replace "Java" with "Type" in "JavaScript".
console.log(name.replace("java" ,"type"));