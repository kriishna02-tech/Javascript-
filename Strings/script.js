//Print the length of a string.

let name = prompt("enter string: ");  //Javascript
console.log(name.length); // it will also count space ,special character  //10

//Convert a string to uppercase.
console.log(name.toUpperCase()); // JAVASCRIPT

//Convert a string to lowercase.
console.log(name.toLowerCase());  //javascript

// Extract the first 5 characters from a string.
console.log(name.substring(0,5));  //javas

//Replace "Java" with "Type" in "JavaScript".
console.log(name.replace("java" ,"type"));  //typescript